import java.io.*;
import java.util.Scanner;

class stringOperations {
    public static void main(String[] args) {
        String s1 = "GFG";
        String s2 = "";
        System.out.println(s1.length()); // length = 3
        System.out.println(s2.length()); // length = 0
        System.out.println("");
        System.out.println(s1.isEmpty()); // isEmpty = false
        System.out.println(s2.isEmpty()); // isEmpty = true
    }
}